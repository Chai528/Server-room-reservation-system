//查看所有预约
void Teacher::showAllOrder() {
	OrderFile of;//拿到所有预约的数据
	if (of.m_Size == 0) {
		cout << "无预约记录！" << endl;
		system("pause");
		system("cls");
		return;
	}

	for (int i = 0; i < of.m_Size; i++) {
		cout << i + 1 << "、";

		cout << "预约日期：周" << of.m_orderData[i]["date"];
		cout << " 时段：" << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午");//三目运算
		cout << " 学号：" << of.m_orderData[i]["stuID"];
		cout << " 姓名：" << of.m_orderData[i]["stuName"];
		cout << " 机房：" << of.m_orderData[i]["roomID"];
		string status = " 状态：";// 0 取消的预约   1 审核中    2 已预约     -1 预约失败
		if (of.m_orderData[i]["status"] == "1") {
			status += "审核中";
		}
		else if (of.m_orderData[i]["status"] == "2") {
			status += "预约成功";
		}
		else if (of.m_orderData[i]["status"] == "-1") {
			status += "审核未通过，预约失败";
		}
		else {
			status += "预约已取消";
		}
		cout << status << endl;
	}

	system("pause");
	system("cls");
}

//审核预约
void Teacher::validOrder() {
	OrderFile of;
	if (of.m_Size == 0){
		cout << "无预约记录！" << endl;
		system("pause");
		system("cls");
		return;
	}
	cout << "待审核记录如下：" << endl;
	int index = 0;
	vector<int>v;
	for (int i = 0; i < of.m_Size; i++){
		if (of.m_orderData[i]["status"] == "1"){
			v.push_back(i);
			cout << ++index << "、";

			cout << "预约日期：周" << of.m_orderData[i]["date"];
			cout << " 时段：" << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午");//三目运算
			cout << " 学号：" << of.m_orderData[i]["stuID"];
			cout << " 姓名：" << of.m_orderData[i]["stuName"];
			cout << " 机房编号：" << of.m_orderData[i]["roomID"];
			string status = " 状态：";// 0 取消的预约   1 审核中    2 已预约     -1 预约失败
			if (of.m_orderData[i]["status"] == "1") {
				status += "审核中";
			}
			cout << status << endl;
		}
	}
	cout << "请输入要审核的预约记录，0代表返回" << endl;
	int select = 0; //接收用户选择的第几条预约记录
	int ret = 0;//接收预约结果记录即 是否 同意
	while (true){
		cin >> select;
		if (select >= 0 && select <= of.m_Size){
			if (select == 0){
				break;
			}
			else{
				cout << "是否同意预约？" << endl;
				cout << "1.同意" << endl;
				cout << "2.不同意"<<endl;
				while (true)
				{
					cin >> ret;
					if (ret == 1) {//通过
						of.m_orderData[v[select - 1]]["status"] = "2";
						break;
					}
					else if (ret == 2){//拒绝
						of.m_orderData[v[select - 1]]["status"] = "-1";
						break;
					}
					cout << "输入有误，请重新输入！" << endl;
				}
				of.updateOrder();//更新信息
				cout << "审核完毕！" << endl;
				break;
			}
		}
		cout << "输入有误，请重新输入！" << endl;
	}
	system("pause");
	system("cls");
}